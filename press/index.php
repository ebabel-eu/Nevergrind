		<h2>About Nevergrind</h2>
		<p>Nevergrind is an indie browser RPG developed by Neverworks Games. Nevergrind is free-to-play and offers hundreds of hours of addictive, fast-paced gameplay. Nevergrind was originally inspired by classic <a href="http://en.wikipedia.org/wiki/EverQuest">Everquest</a>, yet it has a motley blend of influences from many seminal RPGs. The battle format is reminiscent of <a href="http://en.wikipedia.org/wiki/Phantasy_Star_III:_Generations_of_Doom">Phantasy Star III</a>. The real-time combat takes a cue from <a href="http://en.wikipedia.org/wiki/World_of_Warcraft">MMORPGs</a>. The item design is akin to that found in <a href="http://en.wikipedia.org/wiki/Diablo_II">Diablo 2</a>. Nevergrind takes all of these inspirational sources and adds its own unique features, such as a combo rating system.</p>
		
		<p>NeverGrind is set in the world of Vandamor, a land inhabited by orcs, giants and dragons! Battle against the occult forces that have sewn chaos and destruction throughout the world. Nevergrind's gameplay is focused on fighting battles and completing quest objectives throughout four acts. When you first start out, you're just a recruit helping secure the perimeter of Edenburg. Your commander, Miranda Crossheart, sees your potential and keeps you busy with more difficult quests. This leads to your first boss encounter at Lanfeld Outpost, Chief Grimden. In a similar fashion, each act climaxes with a tough boss battle through which more of the game's story is unveiled to the player.</p>

		<p>Treasure-finding is a key feature of Nevergrind's gameplay. Finding rare, powerful items is the best way to make your character more powerful. Since loot is so important in this type of game, a great deal of time and energy was spent designing exciting items. In addition to items with randomized properties, there are more than 800 uniquely designed items that can be discovered. You can even find items from 28 different sets which provide bonuses as you equip more pieces.</p>
		
		<p>The combat system also rewards competence. Completing chain combos is a great way to boost rewards, including experience gained, gold find, and magic find. A chain combo is achieved by killing consecutive monsters without leaving combat. Racking up a huge chain combo requires smart equipment decisions, understanding of the item properties, and masterful command of your class's skills. You can even boost your rewards by fighting multiple enemies at once. As any well designed game should be, taking on big risks leads to big rewards.</p>
		
